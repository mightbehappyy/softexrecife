var num1 = prompt("Digite um numero ")
var num2 = prompt("Digite outro numero ")
var operador = prompt("Digite o operador")
if (operador == "+"){
    println(num1 + num2)
}
if (operador == "-") {
    println(num1 + num2)
}
if (operador == "*"){
    println(num1 * num2)
}
if (operador == "/"){
    println(num1 / num2)
    println("A sobra foi: ", num1 % num2)
}
